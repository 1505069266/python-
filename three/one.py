# 如何转化10进制,8进制,2进制,16进制
# 60秒 = 1分钟

print(0b10)  # 显示二进制  ob表示二进制  10表示数值

print(0o10)  # 显示八进制 0o表示八进制  10表示数值

print(0x1F)  # 显示十六进制  0x表示十六进制  1F表示数值   A10 B11 C12 D13 E14 F15

print(10)  # 显示十进制

# 进制之间如何互相转换  bin(10)

# 任意数值的数转换成二进制: bin(11)

print(bin(11))

print(bin(0x1E))

print(bin(0o22))

# 任意数值转化成十进制: int()

print(int(0b1111))

print(int(0o11))

print(int(0x1F))

# 任意数值转换成十六进制: hex()

print(hex(15))

# 任意数值转换成八进制: oct()

print(oct(8))


